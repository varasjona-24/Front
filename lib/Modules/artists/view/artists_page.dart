import 'dart:io';

import 'package:flutter/material.dart';
import 'package:get/get.dart';

import '../../../app/ui/themes/app_spacing.dart';
import '../../../app/ui/widgets/navigation/app_bottom_nav.dart';
import '../../../app/ui/widgets/navigation/app_top_bar.dart';
import '../../../app/ui/widgets/branding/listenfy_logo.dart';
import '../../../app/ui/widgets/layout/app_gradient_background.dart';
import 'package:flutter_listenfy/Modules/home/controller/home_controller.dart';
import '../controller/artists_controller.dart';
import 'artist_detail_page.dart';
import 'edit_artist_page.dart';

class ArtistsPage extends GetView<ArtistsController> {
  const ArtistsPage({super.key});

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    final scheme = theme.colorScheme;
    final isDark = theme.brightness == Brightness.dark;

    final barBg = Color.alphaBlend(
      scheme.primary.withOpacity(isDark ? 0.24 : 0.28),
      scheme.surface,
    );

    final home = Get.find<HomeController>();

    return Obx(() {
      return Scaffold(
        backgroundColor: Colors.transparent,
        extendBody: true,
        appBar: AppTopBar(
          title: ListenfyLogo(size: 28, color: scheme.primary),
          onSearch: home.onSearch,
        ),
        body: AppGradientBackground(
          child: Stack(
            children: [
              Positioned.fill(
                child: controller.isLoading.value
                    ? const Center(child: CircularProgressIndicator())
                    : ScrollConfiguration(
                        behavior: const _NoGlowScrollBehavior(),
                        child: SingleChildScrollView(
                          padding: EdgeInsets.only(
                            top: AppSpacing.md,
                            bottom: kBottomNavigationBarHeight + 18,
                            left: AppSpacing.md,
                            right: AppSpacing.md,
                          ),
                          child: Column(
                            crossAxisAlignment: CrossAxisAlignment.start,
                            children: [
                              _header(theme),
                              const SizedBox(height: AppSpacing.md),
                              _categoryChips(theme),
                              const SizedBox(height: AppSpacing.lg),
                              _recentArtists(theme),
                              const SizedBox(height: AppSpacing.lg),
                              _summaryRow(theme, context),
                              const SizedBox(height: AppSpacing.md),
                              _artistList(),
                            ],
                          ),
                        ),
                      ),
              ),
              Positioned(
                left: 0,
                right: 0,
                bottom: 0,
                child: DecoratedBox(
                  decoration: BoxDecoration(
                    color: barBg,
                    border: Border(
                      top: BorderSide(
                        color: scheme.primary.withOpacity(isDark ? 0.22 : 0.18),
                        width: 56,
                      ),
                    ),
                  ),
                  child: SafeArea(
                    top: false,
                    child: AppBottomNav(
                      currentIndex: 2,
                      onTap: (index) {
                        switch (index) {
                          case 0:
                            home.enterHome();
                            break;
                          case 1:
                            home.goToPlaylists();
                            break;
                          case 2:
                            home.goToArtists();
                            break;
                          case 3:
                            home.goToDownloads();
                            break;
                          case 4:
                            home.goToSources();
                            break;
                        }
                      },
                    ),
                  ),
                ),
              ),
            ],
          ),
        ),
      );
    });
  }

  Widget _header(ThemeData theme) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          'Artistas',
          style: theme.textTheme.headlineSmall?.copyWith(
            fontWeight: FontWeight.w800,
          ),
        ),
      ],
    );
  }

  Widget _categoryChips(ThemeData theme) {
    final scheme = theme.colorScheme;
    const labels = ['Carpetas', 'Álbumes', 'Artistas', 'Géneros'];

    return SizedBox(
      height: 40,
      child: ListView.separated(
        scrollDirection: Axis.horizontal,
        itemCount: labels.length,
        separatorBuilder: (_, __) => const SizedBox(width: 10),
        itemBuilder: (context, i) {
          final selected = labels[i] == 'Artistas';
          return ChoiceChip(
            label: Text(labels[i]),
            selected: selected,
            onSelected: (_) {},
            showCheckmark: false,
            selectedColor: scheme.surfaceContainerHighest,
            backgroundColor: scheme.surfaceContainerHigh,
            labelStyle: theme.textTheme.bodyMedium?.copyWith(
              color: selected ? scheme.onSurface : scheme.onSurfaceVariant,
              fontWeight: selected ? FontWeight.w700 : FontWeight.w500,
            ),
            shape: RoundedRectangleBorder(
              borderRadius: BorderRadius.circular(20),
            ),
          );
        },
      ),
    );
  }

  Widget _recentArtists(ThemeData theme) {
    return Obx(() {
      final list = controller.filtered.take(8).toList();
      if (list.isEmpty) return const SizedBox.shrink();

      return Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            'Reproducciones recientes',
            style: theme.textTheme.titleLarge?.copyWith(
              fontWeight: FontWeight.w700,
            ),
          ),
          const SizedBox(height: 12),
          SizedBox(
            height: 124,
            child: ListView.separated(
              scrollDirection: Axis.horizontal,
              itemCount: list.length,
              separatorBuilder: (_, __) => const SizedBox(width: 12),
              itemBuilder: (context, i) {
                final artist = list[i];
                return _ArtistCoverCard(artist: artist);
              },
            ),
          ),
        ],
      );
    });
  }

  Widget _summaryRow(ThemeData theme, BuildContext context) {
    return Obx(() {
      final count = controller.filtered.length;
      return Row(
        children: [
          Text(
            '$count artistas',
            style: theme.textTheme.bodyMedium?.copyWith(
              color: theme.colorScheme.onSurfaceVariant,
              fontWeight: FontWeight.w600,
            ),
          ),
          const Spacer(),
          IconButton(
            icon: const Icon(Icons.sort_rounded),
            onPressed: () => _openSortSheet(context),
            tooltip: 'Ordenar',
          ),
        ],
      );
    });
  }

  Widget _artistList() {
    return Obx(() {
      final list = controller.filtered;
      if (list.isEmpty) {
        return Center(
          child: Padding(
            padding: const EdgeInsets.all(AppSpacing.lg),
            child: Text(
              'No hay artistas disponibles.',
              style: Get.textTheme.bodyMedium?.copyWith(
                color: Get.theme.colorScheme.onSurfaceVariant,
              ),
            ),
          ),
        );
      }

      return Column(
        children: [
          for (final artist in list)
            Padding(
              padding: const EdgeInsets.only(bottom: 12),
              child: _ArtistCard(
                artist: artist,
                onOpen: () =>
                    Get.to(() => ArtistDetailPage(artistKey: artist.key)),
                onEdit: () => Get.to(() => EditArtistPage(artist: artist)),
              ),
            ),
        ],
      );
    });
  }

  Future<void> _openSortSheet(BuildContext context) async {
    final theme = Theme.of(context);
    await showModalBottomSheet<void>(
      context: context,
      showDragHandle: true,
      isScrollControlled: true,
      backgroundColor: theme.colorScheme.surface,
      shape: const RoundedRectangleBorder(
        borderRadius: BorderRadius.vertical(top: Radius.circular(24)),
      ),
      builder: (ctx) {
        return Obx(() {
          final sort = controller.sort.value;
          final asc = controller.sortAscending.value;

          return SafeArea(
            child: DraggableScrollableSheet(
              expand: false,
              minChildSize: 0.4,
              initialChildSize: 0.72,
              maxChildSize: 0.9,
              builder: (context, scrollController) {
                return ListView(
                  controller: scrollController,
                  padding: const EdgeInsets.fromLTRB(20, 8, 20, 16),
                  children: [
                    Text(
                      'Ordenar por',
                      style: theme.textTheme.titleLarge?.copyWith(
                        fontWeight: FontWeight.w800,
                      ),
                    ),
                    const SizedBox(height: 8),
                    _SortOption(
                      label: 'Nombre del artista',
                      selected: sort == ArtistSort.name,
                      onTap: () => controller.setSort(ArtistSort.name),
                    ),
                    _SortOption(
                      label: 'Número de canciones',
                      selected: sort == ArtistSort.count,
                      onTap: () => controller.setSort(ArtistSort.count),
                    ),
                    _SortOption(
                      label: 'Aleatorio',
                      selected: sort == ArtistSort.random,
                      onTap: () => controller.setSort(ArtistSort.random),
                    ),
                    const Divider(height: 28),
                    _SortOption(
                      label: 'Tamaño más a menos',
                      selected: !asc,
                      onTap: () => controller.setSortAscending(false),
                    ),
                    _SortOption(
                      label: 'Tamaño menos a más',
                      selected: asc,
                      onTap: () => controller.setSortAscending(true),
                    ),
                    const SizedBox(height: 12),
                    Row(
                      children: [
                        Expanded(
                          child: OutlinedButton(
                            onPressed: () => Navigator.of(ctx).pop(),
                            child: const Text('Cancelar'),
                          ),
                        ),
                        const SizedBox(width: 12),
                        Expanded(
                          child: FilledButton(
                            onPressed: () => Navigator.of(ctx).pop(),
                            child: const Text('Aceptar'),
                          ),
                        ),
                      ],
                    ),
                  ],
                );
              },
            ),
          );
        });
      },
    );
  }
}

class _ArtistCard extends StatelessWidget {
  const _ArtistCard({
    required this.artist,
    required this.onOpen,
    required this.onEdit,
  });

  final ArtistGroup artist;
  final VoidCallback onOpen;
  final VoidCallback onEdit;

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    final scheme = theme.colorScheme;

    final thumb = artist.thumbnailLocalPath ?? artist.thumbnail;

    return Card(
      elevation: 0,
      color: scheme.surfaceContainer,
      shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(18)),
      child: ListTile(
        leading: _ArtistAvatar(thumb: thumb),
        title: Text(artist.name, maxLines: 1, overflow: TextOverflow.ellipsis),
        subtitle: Text('${artist.count} canciones'),
        trailing: IconButton(
          icon: const Icon(Icons.edit_rounded),
          onPressed: onEdit,
        ),
        onTap: onOpen,
      ),
    );
  }
}

class _ArtistCoverCard extends StatelessWidget {
  const _ArtistCoverCard({required this.artist});

  final ArtistGroup artist;

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    final scheme = theme.colorScheme;
    final thumb = artist.thumbnailLocalPath ?? artist.thumbnail;

    final imageProvider = (thumb != null && thumb!.isNotEmpty)
        ? (thumb!.startsWith('http')
              ? NetworkImage(thumb!)
              : FileImage(File(thumb!)) as ImageProvider)
        : null;

    return SizedBox(
      width: 96,
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Container(
            width: 96,
            height: 96,
            decoration: BoxDecoration(
              color: scheme.surfaceContainerHigh,
              borderRadius: BorderRadius.circular(16),
              image: imageProvider != null
                  ? DecorationImage(
                      image: imageProvider,
                      fit: BoxFit.cover,
                    )
                  : null,
            ),
            child: imageProvider == null
                ? Icon(Icons.person_rounded, color: scheme.onSurfaceVariant)
                : null,
          ),
          const SizedBox(height: 6),
          Text(
            artist.name,
            maxLines: 1,
            overflow: TextOverflow.ellipsis,
            style: theme.textTheme.bodyMedium?.copyWith(
              fontWeight: FontWeight.w600,
            ),
          ),
        ],
      ),
    );
  }
}

class _SortOption extends StatelessWidget {
  const _SortOption({
    required this.label,
    required this.selected,
    required this.onTap,
  });

  final String label;
  final bool selected;
  final VoidCallback onTap;

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    final scheme = theme.colorScheme;
    return ListTile(
      contentPadding: EdgeInsets.zero,
      title: Text(
        label,
        style: theme.textTheme.bodyLarge?.copyWith(
          color: selected ? scheme.primary : scheme.onSurface,
          fontWeight: selected ? FontWeight.w700 : FontWeight.w500,
        ),
      ),
      trailing: Icon(
        selected ? Icons.check_circle_rounded : Icons.radio_button_unchecked,
        color: selected ? scheme.primary : scheme.onSurfaceVariant,
      ),
      onTap: onTap,
    );
  }
}

class _ArtistAvatar extends StatelessWidget {
  const _ArtistAvatar({required this.thumb});

  final String? thumb;

  @override
  Widget build(BuildContext context) {
    final scheme = Theme.of(context).colorScheme;

    if (thumb != null && thumb!.isNotEmpty) {
      return CircleAvatar(
        radius: 24,
        backgroundColor: scheme.surface,
        backgroundImage: thumb!.startsWith('http')
            ? NetworkImage(thumb!)
            : FileImage(File(thumb!)) as ImageProvider,
      );
    }

    return CircleAvatar(
      radius: 24,
      backgroundColor: scheme.surface,
      child: Icon(Icons.person_rounded, color: scheme.onSurfaceVariant),
    );
  }
}

class _NoGlowScrollBehavior extends ScrollBehavior {
  const _NoGlowScrollBehavior();

  @override
  Widget buildOverscrollIndicator(
    BuildContext context,
    Widget child,
    ScrollableDetails details,
  ) {
    return child;
  }
}
